package ru.itmo;

public class Prepositions {
    public void and() {
        System.out.print(" и ");
    }

    public void in() {
        System.out.print("в ");
    }

    public void which() {
        System.out.print("которая ");
    }

    public void when() {
        System.out.print("когда ");
    }

    public void how() {
        System.out.print("Как ");
    }

    public void at() {
        System.out.print("у ");
    }

    public void but() {
        System.out.print("a ");
    }

    public void comma() {
        System.out.print(", ");
    }

    public void dot() {
        System.out.println(". ");
    }
    public void dash() {
        System.out.print(" - ");
    }

}
